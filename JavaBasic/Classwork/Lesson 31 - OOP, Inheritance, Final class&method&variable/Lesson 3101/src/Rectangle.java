public class Rectangle {
    private double sideA;
    private double sideB;

    public Rectangle(double sideA, double sideB) {
        this.sideA = sideA;
        this.sideB = sideB;
    }

    @Override
    public String toString() {
        return "Rectangle{" +
                "sideA=" + sideA +
                ", sideB=" + sideB +
                '}';
    }
    public double calcArea(){
        return sideA*sideB;
    }
    public double calcPerimeter(){
        return (sideA+sideB)*2;
    }
}
